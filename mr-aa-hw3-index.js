function hw3_get_img_url ( page) {
	if ( page < 1) page = 1;
	if ( page > hw3.length - 1) page = hw3.length - 1;
	cur_hw_page = page;
	var p = page;
	var prefixed_page = p;
	if ( p < 10) prefixed_page = "00" + p;
	else if ( p < 100) prefixed_page = "0" + p;
	return "hw3/" + Math.floor(page/100) + "/hw-p" + prefixed_page + ".png";
}

